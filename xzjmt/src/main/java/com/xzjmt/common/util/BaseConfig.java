package com.xzjmt.common.util;

public class BaseConfig {
	private String WWWROOT;
	private String FILESROOT;
	private String IMGROOT;
	private String STATICPCROOT;
	
	public String getWWWROOT() {
		return WWWROOT;
	}
	public void setWWWROOT(String wWWROOT) {
		WWWROOT = wWWROOT;
	}
	public String getFILESROOT() {
		return FILESROOT;
	}
	public void setFILESROOT(String fILESROOT) {
		FILESROOT = fILESROOT;
	}
	public String getIMGROOT() {
		return IMGROOT;
	}
	public void setIMGROOT(String iMGROOT) {
		IMGROOT = iMGROOT;
	}
	public String getSTATICPCROOT() {
		return STATICPCROOT;
	}
	public void setSTATICPCROOT(String sTATICPCROOT) {
		STATICPCROOT = sTATICPCROOT;
	}
}
